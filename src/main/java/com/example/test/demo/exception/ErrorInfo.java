package com.example.test.demo.exception;

/**
 * The type Error info.
 */
public class ErrorInfo {

    private String msg;

    /**
     * Instantiates a new Error info.
     */
    public ErrorInfo() {
    }

    /**
     * Instantiates a new Error info.
     *
     * @param msg the msg
     */
    public ErrorInfo(String msg) {
        this.msg = msg;
    }

    /**
     * Gets msg.
     *
     * @return the msg
     */
    public String getMsg() {
        return msg;
    }

    /**
     * Sets msg.
     *
     * @param msg the msg
     */
    public void setMsg(String msg) {
        this.msg = msg;
    }
}
